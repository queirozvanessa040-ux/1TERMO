# Calcular gorjetas receba o valor da conta de um restaurante e retorne o valor da gorjeta(considerando 10% do valor da conta).

print("Bem-Vindo ao Restaurante da TiaJuca!")
conta = float(input("Digite o valor da sua conta:"))
gorjeta = float(input("Digite o valor da sua gorjeta:"))
total = conta / gorjeta + conta
print("O valor total da compra foi?", total)